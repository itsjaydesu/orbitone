export function getLocalizedTrackTitle(item: MidiLibraryItem, language: AppLanguage) {
  if (language === 'en') {
    return item.title
  }

  return TRACK_TITLE_TRANSLATIONS_JA[item.id] ?? item.title
}

export function getLocalizedTrackSubtitle(subtitle: string | null | undefined, language: AppLanguage) {
  if (!subtitle) {
    return null
  }

  return TRACK_SUBTITLE_TRANSLATIONS[language][subtitle] ?? subtitle
}
